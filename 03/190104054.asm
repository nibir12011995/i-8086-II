SSEG SEGMENT STACK 'STACK'
     DW 50 DUP(?)
SSEG ENDS

DSEG SEGMENT 'DATA'
CHARS DB 00000111b, 01111100b, 01001111b, 01110110b, 01011011b         
LEDs DB 00000100b, 00001100b, 00001110b, 00000100b, 00000100b
DSEG ENDS

CSEG SEGMENT 'CODE'
MAIN PROC FAR

    PUSH DS
    MOV AX, 0
    PUSH AX

    MOV AX, DSEG
    MOV DS, AX
    MOV ES, AX

		
	XOR SI, SI
	
	MOV BX, 5

NEXT:
    MOV DX, 2030h
	ADD DX, SI
	MOV AL,CHARS[SI]
	OUT DX,AL
	
	
    MOV DX, 2070h
	MOV AL,LEDs[SI]
	OUT DX,AL   
	

    ;for delay 
	 
	MOV CX, 01FFH
    LO: 
    LOOP LO
 
	 
	INC SI  
    
    DEC BX
    CMP BX, 0
    JG NEXT 
    	

RET
MAIN ENDP
CSEG ENDS 
END MAIN  

